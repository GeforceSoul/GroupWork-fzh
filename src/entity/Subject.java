package entity;

public class Subject {
	private int NewsID;
	private String NewsTitle;
	private String NewsText;
	private String NewsDateTime;
	private int AnID;
	private String AnTitle;
	private String AnText;
	private String AnDateTime;
	
	public int getNewsID() {
		return NewsID;
	}
	public void setNewsID(int newsID) {
		NewsID = newsID;
	}
	public String getNewsTitle() {
		return NewsTitle;
	}
	public void setNewsTitle(String newsTitle) {
		NewsTitle = newsTitle;
	}
	public String getNewsText() {
		return NewsText;
	}
	public void setNewsText(String newsText) {
		NewsText = newsText;
	}
	public String getNewsDateTime() {
		return NewsDateTime;
	}
	public void setNewsDateTime(String newsDateTime) {
		NewsDateTime = newsDateTime;
	}
	public int getAnID() {
		return AnID;
	}
	public void setAnID(int anID) {
		AnID = anID;
	}
	public String getAnTitle() {
		return AnTitle;
	}
	public void setAnTitle(String anTitle) {
		AnTitle = anTitle;
	}
	public String getAnText() {
		return AnText;
	}
	public void setAnText(String anText) {
		AnText = anText;
	}
	public String getAnDateTime() {
		return AnDateTime;
	}
	public void setAnDateTime(String anDateTime) {
		AnDateTime = anDateTime;
	}
}
